Type.registerNamespace("Telerik.Web.UI.Scheduler");
(function(){var d=$telerik.$;
var a=Telerik.Web.UI;
var c=60000;
d.registerEnum(a,"DayOfWeek",{Sunday:0,Monday:1,Tuesday:2,Wednesday:3,Thursday:4,Friday:5,Saturday:6});
var b=a.Scheduler.DateTime=function(e){if(!e){this._date=new Date();
return;
}if(e.getTime){this._date=new Date(e.getTime());
}else{this._date=new Date(e);
}};
b.add=function(g,e){var f=e.get_ticks?e.get_ticks():e;
return b._addTicks(g,f);
};
b.subtract=function(h,g){g=new b(g).toDate();
var e=h.getTime()-g;
var f=h.getTimezoneOffset()-g.getTimezoneOffset();
return e-(f*c);
};
b.areEqual=function(e,f){return(!(e>f||f>e));
};
b.getDate=function(e){return new Date(e.getFullYear(),e.getMonth(),e.getDate());
};
b.getTimeOfDay=function(e){return b.subtract(e,b.getDate(e));
};
b._addTicks=function(i,h){var g=i.getTimezoneOffset();
var f=new Date(i.getTime()+h);
var e=f.getTimezoneOffset()-g;
return new Date(f.getTime()+e*c);
};
a.Scheduler.DateTime.prototype={get_date:function(){return new b(b.getDate(this._date));
},get_timeOfDay:function(){return b.getTimeOfDay(this._date);
},add:function(e){return new b(b.add(this._date,e));
},subtract:function(e){return b.subtract(this._date,e);
},toDate:function(){return this._date;
}};
a.Scheduler.TimeSpan=function(e){this._ticks=e||0;
};
a.Scheduler.TimeSpan.prototype={get_ticks:function(){return this._ticks;
}};
})();
